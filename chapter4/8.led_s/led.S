#define GPD0CON 0xE02000A0
#define GPD0DAT 0xE0200244

.global _start		//把_start链接属性改为外部，这样其他文件能够看见
 _start:
	// 第一步：把0x11111111写入0xE02000A0(GPD0CON)位置，把所有引脚设置为输出，代码不变
	ldr r0, =0x11111111			// 从“=”看出用的是ldr伪指令，需要编译器判断这个数是
	ldr r1, =GPD0CON			// 合法立即数还是非法立即数。一般都用ldr伪指令
	str r0, [r1]				// 寄存器间接寻址，功能：把r0中的数写入到r1中的数为地址的内存中去
	
	// 第二步：把中间LED (GPD0_1)的输出设为0，其余两颗设为1，其他位不管 
	ldr r0, =0x0				//相当于0x00000028
	ldr r1, =GPD0DAT
	str r0, [r1]				// 把0写入GPJ0DAT寄存器中断，引脚即输出低电平，LED点亮

	b .							//“.”代表当前这一句指令的地址，也是一句死循环（高大上）
