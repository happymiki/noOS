 _start:
	// 第一步：把0x11111111写入0xE0200240(GPJ0CON)位置
	ldr r0, =0x11111111			// 从“=”看出用的是ldr伪指令，需要编译器判断这个数是
	ldr r1, =0xE0200240			// 合法立即数还是非法立即数。一般都用ldr伪指令
	str r0, [r1]				// 寄存器间接寻址，功能：把r0中的数写入到r1中的数为地址的内存中去
	
	// 第二步：把0x00000000写入0xE0200244(GPI0DAT)位置
	ldr r0, =0x00000000
	ldr r1, =0xE0200244
	str r0, [r1]				// 把0写入GPJ0DAT寄存器中断，引脚即输出低电平，LED点亮
	
flag:							// 这两行写了一个死循环,为了不让CPU跑飞
	b flag
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 